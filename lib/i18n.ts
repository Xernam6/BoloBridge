type SupportedLang = keyof typeof translations;

/**
 * Get a translated string for the given key and language.
 * Falls back to English if the key is not found in the target language.
 */
export function t(key: TranslationKey, lang: string = 'en'): string {
  const l = (lang in translations ? lang : 'en') as SupportedLang;
  return (translations[l] as Record<string, string>)?.[key] ?? translations.en[key] ?? key;
}

export default translations;
